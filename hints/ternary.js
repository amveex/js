// three operands below
// condition
// ? trueExpression
// : falseExpression

let score = 80;
let scoreRating;

// first example

if(score > 70) {
    scoreRating = "Excellent";
} else {
    scoreRating = "Can do better";
}

// second example

scoreRating = score > 70 ? "Excellent" : "Can do better";
